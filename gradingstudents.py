def gradingStudents(grades):
    newg = list()
    for _ in grades:
        next_multiple = (_//5 + 1) * 5
        if _ < 38 or next_multiple - _ >= 3: pass
        else: _ =  next_multiple 
        newg.append(_)
    return newg

